# 이미지에 사용할 거북이 이미지 라이브러리
import turtle

# shape : 형태
turtle.shape('turtle')

# forward : (위치)앞으로 진행
# right : 오른쪽 회전 (각도)

turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)

# 이미지 창이 닫히지 않고 계속 유지 된다.
turtle.done()