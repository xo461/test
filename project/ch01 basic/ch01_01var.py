# a = 100
# b = 50
# input()함수 - 사용자에게 문자열로 데이터를 입력 받는다.
a = int(input("첫번째 숫자를 입력하세요 : "))
b = int(input("두번째 숫자를 입력하세요 : "))
result = a + b
print(a,' + ', b, ' = ', result)

result = a - b
print(a,' - ', b, ' = ', result)

result = a * b
print(a,' * ', b, ' = ', result)

result = a / b
print(a,' / ', b, ' = ', result)
print(a, '-', b)
